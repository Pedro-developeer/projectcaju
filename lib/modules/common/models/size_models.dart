class SizeModel {
  SizeModel({
    required this.medium,
    this.tiny,
    this.extraSmall,
    this.small,
    this.large,
    this.extraLarge,
    this.huge,
    this.gigantic,
    this.extraGigantic,
  });

  final double? tiny;
  final double? extraSmall;
  final double? small;
  final double medium;
  final double? large;
  final double? extraLarge;
  final double? huge;
  final double? gigantic;
  final double? extraGigantic;
}